    <script type="text/javascript" language="javascript" >  
 	 $(document).ready(function(){  
      $('#add_button').click(function(){  
           $('#user_form')[0].reset();  
           $('.modal-title').text("Paramètrage des émissions");  
           $('#action').val("Add");  
      });

      var dataTable = $('#user_data').DataTable({  
           "processing":true,  
           "serverSide":true,  
           "order":[],  
           "ajax":{  
                url:"<?php echo base_url() . 'admin/fetch_emission'; ?>",  
                type:"POST"  
           },  
           "columnDefs":[  
                {  
                     "targets":[0, 0, 0],  
                     "orderable":false,  
                },  
           ],  
      });

      $(document).on('submit', '#user_form', function(event){  
           event.preventDefault(); 
           var nom_emission 				= $('#nom_emission').val();
           var heure_debit					= $('#heure_debit').val();
           var heure_fin					= $('#heure_fin').val();
           var description_emisssion		= $('#description_emisssion').val();
           
           var action = $('#action').val();

           if(nom_emission != '' && heure_debit != '' && heure_fin != '' && description_emisssion !='')
            {

	            if (action =="Add") {
	                 
	                $.ajax({  
	                     url:"<?php echo base_url() . 'admin/insertion_emission'?>",  
	                     method:'POST',  
	                     data:new FormData(this),  
	                     contentType:false,  
	                     processData:false,  
	                     success:function(data)  
	                     {  
	                          swal("succès", ''+data, 'success');  
	                          $('#user_form')[0].reset();  
	                          $('#userModal').modal('hide');  
	                          dataTable.ajax.reload();  
	                     }  
	                });
	                  // alert("insertion");

	            }
		        else if (action == 'Edit') {
		        	$.ajax({  
                         url:"<?php echo base_url() . 'admin/modification_emission'?>",  
                         method:'POST',  
                         data:new FormData(this),  
                         contentType:false,  
                         processData:false,  
                         success:function(data)  
                         {  
                              swal("succès", ''+data, 'success');  
                              $('#user_form')[0].reset();  
                              $('#userModal').modal('hide');  
                              dataTable.ajax.reload();  
                         }  
                    });
	            }

            }
            else
            {
            	alert("Tous les champs doivent être remplis ");
            	// swal("Tous les champs doivent être remplis", "erreur", "alert-danger"); 
            }
             
      }); 

      $(document).on('click', '.update', function(){  
           var id_emission = $(this).attr("id_emission");  
           $.ajax({  
                url:"<?php echo base_url(); ?>admin/fetch_single_emission",  
                method:"POST",  
                data:{id_emission:id_emission},  
                dataType:"json",  
                success:function(data)  
                {  
                     $('#userModal').modal('show');  
                     $('#nom_emission').val(data.nom_emission);
                     $('#heure_debit').val(data.heure_debit);
                     $('#heure_fin').val(data.heure_fin);
                     $('#description_emisssion').val(data.description_emisssion);
                     
                     $('.modal-title').text("Modification de l'émission");  
                     $('#id_emission').val(id_emission);  
                     $('#action').val("Edit");  
                }  
           });  
      });

      $(document).on('click', '.delete', function(){
          var id_emission = $(this).attr("id_emission");

          	swal({
		        title: "Êtes-vous sûr?",
		        text: "Vous ne pourrez pas récupérer ces données provinciales!",
		        type: "warning",
		        showCancelButton: true,
		        confirmButtonColor: "#dc3545",
		        confirmButtonText: "Oui, supprimez-le!",
		        cancelButtonText: "Non, annulez!",
		        closeOnConfirm: false,
		        closeOnCancel: false
		    }, function (isConfirm) {
		        if (isConfirm) {

		        	$.ajax({
		              url:"<?php echo base_url(); ?>admin/supression_emission",
		              method:"POST",
		              data:{id_emission:id_emission},
		              success:function(data)
		              {
		                 swal("succès!", ''+data, "success");
		                 dataTable.ajax.reload();
		              }
		            });
		           
		        } else {
		            swal("Ouf!!!", "opération annulée :)", "error");
		        }
		    });
      });

	
 });  
 </script>

